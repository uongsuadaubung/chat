// Use localStorage if available, default to 'vi'
const getInitialLang = (): Language => {
  if (typeof window === 'undefined') return 'vi';
  const saved = localStorage.getItem('lang');
  if (saved === 'vi' || saved === 'en') return saved;
  return 'vi';
};

class I18nStore {
  locale = $state<Language>(getInitialLang());

  get t() {
    return (key: string) => {
      const locale = this.locale;
      const dict = (translations[locale] || translations['vi']) as Record<string, string>;

      // 1. Precise match
      if (dict[key]) return dict[key];

      // 2. Backward compatibility: convert snake_case (legacy DB) to camelCase
      if (key.includes('_')) {
        const camelKey = key.replace(/_([a-z])/g, (_, letter) => letter.toUpperCase());
        if (dict[camelKey]) return dict[camelKey];
      }

      // 3. Fallback to 'vi' if current locale failed
      if (locale !== 'vi') {
        const viDict = translations['vi'] as Record<string, string>;
        if (viDict[key]) return viDict[key];
        if (key.includes('_')) {
          const camelKey = key.replace(/_([a-z])/g, (_, letter) => letter.toUpperCase());
          if (viDict[camelKey]) return viDict[camelKey];
        }
      }

      return key;
    };
  }

  setLocale(lang: Language) {
    this.locale = lang;
    if (typeof window !== 'undefined') {
      localStorage.setItem('lang', lang);
    }
  }
}

export const i18n = new I18nStore();
